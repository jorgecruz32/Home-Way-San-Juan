<p class="t1"><b>¿Qué es una colcha?</b></p><br>
<p class="con1">Una colcha es un cubrecama. Sus funciones son decorar la cama y por tanto toda la habitación y proteger del frío.

Es cierto que en un principio las colchas sólo tenían una función decorativa. Se ponían encima de la cama durante el día para decorar la habitación y se retiraban por la noche.

Sin embargo, la colcha ha ido evolucionado y ya podemos decir que se trata de una prenda más de abrigo y que ofrecen el mismo confort como por ejemplo las fundas nórdicas.</p><br>

<p class="t2"><b>Un juego de sábanas incluye:</b></p><br>
<p class="con2">
Una sábana encimera. Es la que se coloca encima de la anterior, debajo de la manta. Se caracteriza por el diseño particular de uno de los extremos que actuará de embozo, quedando por tanto a la vista.
Aun no siendo una sábana, la funda de almohada suele venderse en conjunto con las piezas anteriores para armonizar en diseño.
</p> <br>

<p class="t3"><b>¿Qué son las cortinas?</b></p><br>
<p class="con3">
Están hechas principalmente de telas de colores llamativos y algunos oscuros. Su función principal es la de impedir total o parcialmente el paso de la luz y la visibilidad desde el exterior. También se pueden utilizar detrás de una puerta de acceso.
</p><br>

<p class="t4"><b>¿Qué son las cubresalas?</b></p><br>
<p class="con4">
Para poder adaptarse a los diferentes diseños y estilos de sillones, los cubresalas se componen de 2 partes
<br>
a) Funda principal. Es un lienzo de tela rectangular. Hay que extenderlo como al doblar una sábana y centrarlo sobre sillón para que cubra frente, respaldo y costados.
<br>
b) Faldón. Es una tela elástica que rodea el sillón en su parte interior.
<br>
Para ayudar a que el cubresala tome mejor la forma del sillón se recomienda «fajar» o meter partes de la tela en los espacios asiento-descansa brazos y asiento-respaldo. También puede apoyarse de pasadores para limitar el movimiento.
</p><br>


<p class="t5"><b>Tipos de Cobertores de cama</b></p><br>
<p class="con5">
Hay dos distintos tipos de cobertores de cama: los de invierno/otoño y los de primavera/verano. Ambos tienen distinto grosos y están hechos de diferentes telas, porque así pueden alternarse según la estación.

Tener dos diferentes es una forma de renovar la habitación cada pocos meses, pero otra excelente opción es elegir cobertores de cama que se pondrán encima de todas las cobijas que se elijan, así sean colchas nórdicas o solo sábanas, de forma que el cuarto lucirá muchísimo siempre, sin necesidad de cambiar la apariencia de la cama.
</p><br>

